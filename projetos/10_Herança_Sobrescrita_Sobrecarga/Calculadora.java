
public class Calculadora {

	public Calculadora() {		
	}
	
	public int somar(int n1, int n2) {
		return n1 + n2;
	}

	public int somar(int n1, int n2, int n3) {
		return n1 + n2 + n3;
	}
	
	public float somar(int n1, float n2) {
		return n1 + n2;
	}
	
	public float somar(float n1, float n2) {
		return n1 + n2;
	}	
}
